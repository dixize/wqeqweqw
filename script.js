function openSlide(id) {
    const s = slideData[id];
    const content = document.getElementById('modal-content');
    
    // Генерируем контент с анимацией animate__fadeInUp для каждого блока
    content.innerHTML = `
        <h2 class="slide-title animate__animated animate__fadeInDown">${s.title}</h2>
        
        <div class="info-block animate__animated animate__fadeInUp animate__delay-1s">
            <span class="highlight-header">Предпосылки развития</span>
            <ul class="text-list">
                ${s.pre.map(p => `<li>${p}</li>`).join('')}
            </ul>
        </div>

        <div class="info-block animate__animated animate__fadeInUp animate__delay-1s">
            <span class="highlight-header">Область применения</span>
            <ul class="text-list">
                ${s.areas.map(a => `<li><strong>${a.t}</strong>${a.d}</li>`).join('')}
            </ul>
        </div>

        <div class="info-block animate__animated animate__fadeInUp animate__delay-2s">
            <span class="highlight-header">Эффекты</span>
            <div class="effects-grid">
                ${s.effects.map(e => `<div class="effect-card"><span class="effect-val">${e}</span></div>`).join('')}
            </div>
        </div>

        <div class="info-block animate__animated animate__fadeInUp animate__delay-2s">
            <span class="highlight-header">Опыт внедрения</span>
            <div class="partner-cloud">
                ${s.partners.map(p => `<span class="p-badge">${p}</span>`).join('')}
            </div>
        </div>
    `;

    document.getElementById('modal').classList.remove('hidden');
    document.body.style.overflow = 'hidden';
}

function closeSlide() {
    document.getElementById('modal').classList.add('hidden');
    document.body.style.overflow = 'auto';
}
